#Calificacion cualitativa
calificacion = int(input("Ingrese la calificacion: "))
if calificacion >=0 and calificacion <=70:
    print("Reprobado")
elif calificacion >70 and calificacion <=80:
    print("Regular")
elif calificacion >80 and calificacion <=90:
    print("Bien")
elif calificacion >90 and calificacion <=100:
    print("Excelente")        